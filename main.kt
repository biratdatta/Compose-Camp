Kotlin is a statically typed programming language. It means that the compiler checks the type of the variable at compile time. The compiler throws an error if the variable is assigned a value of a different type.


// Kotlin program to illustrate the use of var and val

fun main() {
    var name = "Android"
    name = "Kotlin"
    println(name)
}

// Output

Kotlin

The above code is a simple Kotlin program that illustrates the use of var and val. The var keyword is used to declare a variable. The val keyword is used to declare a constant. The value of a constant cannot be changed once it is assigned. The value of a variable can be changed.


